-- Create Customer Table --

CREATE TABLE CUSTOMER(
CustomerID INT PRIMARY KEY,
CustomerType Varchar(15) NULL,
LastName Varchar(30) NOT NULL,
FirstName Varchar(30) NOT NULL,
Address Varchar(30) NOT NULL,
City Varchar(15) NOT NULL,
State Char(2) NOT NULL,
ZIP Char(5) NOT NULL,
PhoneNumber Char(12),
Email VarChar(50));


-- Create Employee_Position Table --

CREATE TABLE EMPLOYEE_POSITION(
PositionID INT PRIMARY KEY,
Title Varchar(30) NOT NULL);
CREATE TABLE WAREHOUSE(
WarehouseID INT PRIMARY KEY,
Name Varchar(20) NOT NULL,
Address Varchar(30) NOT NULL,
City Varchar(15) NOT NULL,
State Char(2) NOT NULL,
ZIP Char(5) NOT NULL,
PhoneNumber Char(12));


-- Create StorageUnitType Table --

CREATE TABLE STORAGEUNITTYPE(
UnitTypeID INT PRIMARY KEY,
Type Char(15));


-- Create Vehicle Table --

CREATE TABLE VEHICLE(
VehicleID INT PRIMARY KEY,
Type Varchar(7),
LicensePlate Char(7),
AxelCount INT,
Color Varchar(10),
WareHouseID INT NOT NULL REFERENCES WAREHOUSE(WareHouseID));


-- Create StorageUnit Table --

CREATE TABLE STORAGEUNIT(
UnitID INT PRIMARY KEY,
WarehouseID INT NOT NULL REFERENCES WAREHOUSE(WarehouseID),
UnitTypeID INT NOT NULL REFERENCES STORAGEUNITTYPE(UnitTypeID),
Size Varchar(8),
RentPerMonth Decimal(10,2) NOT NULL); 


-- Create Employee Table --

CREATE TABLE EMPLOYEE( 
EmployeeID INT PRIMARY KEY,
LastName Varchar(30) NOT NULL,
FirstName Varchar(30) NOT NULL,
Address Varchar(30) NOT NULL,
City Varchar(15) NOT NULL,
State Char(2) NOT NULL,
ZIP Char(5) NOT NULL,
PhoneNumber Char(12),
CellNumber Char(12),
SSNumber Char(12),
DOB DATE NOT NULL,
HireDate DATE NOT NULL,
TerminationDate DATE NULL,
PositionID int NOT NULL REFERENCES EMPLOYEE_POSITION(PositionID),
AnnualSalary Decimal(10,2),
WarehouseID int NOT NULL REFERENCES WAREHOUSE(WarehouseID));


-- Create Driver_Rating Table --

CREATE TABLE DRIVER_RATING(
RatingID INT PRIMARY KEY,
Grade Char(1));
CREATE TABLE DRIVER(
DriverID INT PRIMARY KEY,
LastName Varchar(30) NOT NULL,
FirstName Varchar(30) NOT NULL,
RatingID INT NOT NULL REFERENCES DRIVER_RATING(RatingID),
DriverPhoneNumber Char(12) NOT NULL,
CompensationPerMile DECIMAL(10,2) NOT NULL);


-- Create JobOrder Table --

CREATE TABLE JOBORDER(
OrderID INT PRIMARY KEY,
CustomerID INT NOT NULL REFERENCES CUSTOMER(CustomerID),
OrderDate Date,
JobCost Decimal(10,2) NOT NULL)


-- Create JobOrder_Detail Table --

CREATE TABLE JOBORDER_DETAIL(
Detail_ID INT PRIMARY KEY,
JobOrderID INT NOT NULL REFERENCES JOBORDER(OrderID),
DriverID INT NOT NULL REFERENCES DRIVER(DriverID),
VehicleID INT NOT NULL REFERENCES VEHICLE(VehicleID),
JobDate DATE NOT NULL,
StartAddress VARCHAR(30) NOT NULL,
StartCity VARCHAR(20) NOT NULL,
StartState CHAR(2) NOT NULL,
startZIP CHAR(5) NOT NULL,
EndAddress VARCHAR(30) NOT NULL,
EndCity VARCHAR(20) NOT NULL,
EndState CHAR(2) NOT NULL,
EndZip CHAR(5) NOT NULL,
DetailCost DECIMAL(10,2),
Miles INT NOT NULL,
Weight INT NOT NULL);


-- Create EmployeeJobDetail Table --

CREATE TABLE EMPLOYEEJOBDETAIL(
JobDetailID INT NOT NULL REFERENCES JOBORDER_DETAIL(Detail_ID),
EmployeeID INT NOT NULL REFERENCES EMPLOYEE(EmployeeID));


-- Create RentalOrder Table --

CREATE TABLE RENTALORDER(RentalOrderID INT PRIMARY KEY,
CustomerID INT NOT NULL REFERENCES CUSTOMER(CustomerID),
RentalOrderDate DATE,
RentalCost DECIMAL(10,2))


-- Create RentalDetail Table --

CREATE TABLE RENTALDETAIL(
Rental_ID INT PRIMARY KEY,
RentalOrderID INT NOT NULL REFERENCES RENTALORDER(RentalOrderID),
UnitID INT NOT NULL REFERENCES STORAGEUNIT(UNITID),
RentalStartDate DATE,
RentalEndDate Date);



